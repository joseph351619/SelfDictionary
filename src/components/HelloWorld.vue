          <v-text-field
            v-model="newExplanation"
            outlined
            clearable
            label="Explanation"
            type="text"
          >
            <template v-slot:append-outer>
              <v-btn text @click="addExplanation" color="primary">Add</v-btn>
            </template>
          </v-text-field>
      </v-col>

      <v-col cols="12">
        <v-alert type="info" v-if="!searchResult.length">
          Word not found.
        </v-alert>
        <v-expansion-panels>
          <v-expansion-panel
            v-for="(item,i) in searchResult"
            :key="i"
          >
            <ExplainPad v-bind:word="searchText" v-bind:explain="item.val().explanation" v-bind:explainDate="item.val().date" v-bind:explainKey="item.key" v-bind:sentences="getGenerator(item)"></ExplainPad>
          </v-expansion-panel>
